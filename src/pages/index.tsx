import React from 'react'
import Head from 'next/head'
import { GetStaticProps } from 'next'

import { format, parseISO } from 'date-fns'
import ptBR from 'date-fns/locale/pt-BR'

import { api } from '../services/api'
import { formatedDurationTimeEpisode } from '../utils/formatedDurationTimeEpisode'

import { Container } from '../styles/pages/index'

interface episode {
  id: string
  title: string
  members: string
  thumbnail: string
  description: string
  publishedAt: Date
  url: string
  type: string
  duration: number
}

interface episodeJSON {
  id: string
  title: string
  members: string
  thumbnail: string
  description: string
  published_at: string
  file: {
    url: string
    type: string
    duration: number
  }
}

interface HomeDataProps {
  episodes: Array<episode>
}

const Home: React.FC<HomeDataProps> = episodes => {
  return (
    <div>
      <Head>
        <title>prisma.cast | O melhor para vocÃª ouvir, sempre!</title>

        <meta name="description" content="Generated by create next app" />
      </Head>
      <Container>{JSON.stringify(episodes)}</Container>
    </div>
  )
}

export default Home

export const getStaticProps: GetStaticProps = async () => {
  const { data } = await api.get('episodes', {
    params: {
      _limit: 12,
      _sort: 'published_at',
      _order: 'desc'
    }
  })

  const episodesFormated: episode = data.map(function (ep: episodeJSON) {
    return {
      id: ep.id,
      title: ep.title,
      members: ep.members,
      thumbnail: ep.thumbnail,
      description: ep.description,
      publishedAt: format(parseISO(ep.published_at), 'd MMM yy', {
        locale: ptBR
      }),
      url: ep.file.url,
      type: ep.file.type,
      duration: ep.file.duration,
      durationFormated: formatedDurationTimeEpisode(ep.file.duration)
    }
  })

  return {
    props: {
      episodes: episodesFormated
    }
  }
}
